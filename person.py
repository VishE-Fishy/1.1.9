p = trtl.Turtle()
p.pensize(3)
p.speed(0)

p.fillcolor("red")
p.begin_fill()
p.left(45)
p.circle(50, 270)
loco = p.pos()
p.heading()
p.penup()
p.goto(0,0)
p.pendown()
p.left(90)
p.forward(100)
p.right(90)
p.circle(10, 270)
p.forward(85)

p.penup()
p.goto(loco)
p.pendown()
p.setheading(0)
p.left(130)
p.forward(100)
p.left(90)
p.circle(-10, 270)
p.forward(85)

p.right(40)
p.forward(150)
p.right(15)
p.forward(100)
p.right(90)
p.circle(10, 270)
p.forward(85)

p.setheading(0)
p.forward(70)

p.penup()
p.forward(10)
p.pendown()
loco = p.pos()

p.right(45)
p.forward(100)
p.left(90)
p.circle(-10, 270)
p.forward(120)

p.penup()
p.goto(loco)
p.pendown()
p.setheading(0)
p.left(90)
p.forward(160)
p.end_fill()
